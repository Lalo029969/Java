// Esta es una clase pública llamada HelloWorld.

// En Java, todo el código debe estar dentro de una clase.
public class HolaMundo {

    // Este es el método principal (main), el punto de entrada de cualquier programa en Java.
    // Cuando ejecutas el programa, Java empieza a correr desde aquí.
    public static void main(String[] args) {

        // Esta línea imprime "Hola Mundo" en la consola.
        // System.out es un objeto que representa la salida estándar (la pantalla).
        // println() es un método que imprime el texto y luego hace un salto de línea.
        System.out.println("Hola Mundo");
    }
}



